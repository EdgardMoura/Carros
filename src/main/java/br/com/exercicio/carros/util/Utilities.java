package br.com.exercicio.carros.util;

public class Utilities {
	
	public int Sun(int a, int b) {
		
		return a + b;
	}
public int Sub(int a, int b) {
		
		return a - b;
	}
public double Div(double a, double b) {
	if (b != 0) {
	return a / b;
}
	else {return 0;}
}
public int Mul(int a, int b) {
	
	return a * b;
}

}
